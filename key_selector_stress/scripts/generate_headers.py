#!/usr/bin/env python3
"""Generate compile-time stress-test headers for key_selector PR #2776."""

from pathlib import Path

OUT = Path(__file__).resolve().parent.parent / "generated"


def key_name(i: int) -> str:
    # Spread first bytes so the compile-time perfect hash is tractable at 250 keys.
    return f"f{i:02x}_{i:03d}"


def long_key(prefix: str, total_len: int) -> str:
    if len(prefix) >= total_len:
        return prefix[:total_len]
    return prefix + ("x" * (total_len - len(prefix)))


def write_many_keys_header(n: int) -> None:
    keys = [key_name(i) for i in range(n)]
    key_args = ", ".join(f'"{k}"' for k in keys)
    lines = [
        "// Auto-generated by scripts/generate_headers.py — do not edit.",
        "#pragma once",
        "#include <array>",
        "#include <cstddef>",
        "#include <cstdint>",
        "#include <string>",
        "#include <vector>",
        "#include <algorithm>",
        "#include <random>",
        "#include \"simdjson.h\"",
        "",
        f"namespace generated_many_keys_{n} {{",
        "",
        f"inline constexpr std::size_t key_count = {n};",
        "",
        "inline std::string key_name(std::size_t i) {",
        "  char buf[16];",
        '  std::snprintf(buf, sizeof(buf), "f%02zx_%03zu", i, i);',
        "  return buf;",
        "}",
        "",
        f"using selector = simdjson::ondemand::key_selector<{key_args}>;",
        "",
        "inline std::string build_json_ordered(bool reverse = false) {",
        "  std::string json = \"{\";",
        "  for (std::size_t i = 0; i < key_count; ++i) {",
        "    std::size_t idx = reverse ? (key_count - 1 - i) : i;",
        f"    if (i) json += \",\";",
        f"    json += \"\\\"\" + std::string(key_name(static_cast<int>(idx))) + \"\\\":\";",
        "    json += std::to_string(idx);",
        "  }",
        "  json += \"}\";",
        "  return json;",
        "}",
        "",
        "inline std::string build_json_with_noise(std::size_t extra_keys) {",
        "  std::string json = \"{\";",
        "  bool first = true;",
        "  for (std::size_t n = 0; n < extra_keys; ++n) {",
        "    if (!first) json += \",\";",
        "    first = false;",
        "    json += \"\\\"noise_\" + std::to_string(n) + \"\\\":\" + std::to_string(n * 17);",
        "  }",
        "  for (std::size_t i = 0; i < key_count; ++i) {",
        "    if (!first) json += \",\";",
        "    first = false;",
        f"    json += \"\\\"\" + std::string(key_name(static_cast<int>(i))) + \"\\\":\";",
        "    json += std::to_string(i * 3);",
        "  }",
        "  json += \"}\";",
        "  return json;",
        "}",
        "",
        "inline std::string build_json_shuffled(uint64_t seed) {",
        "  std::vector<std::size_t> order(key_count);",
        "  for (std::size_t i = 0; i < key_count; ++i) order[i] = i;",
        "  std::mt19937_64 rng(seed);",
        "  std::shuffle(order.begin(), order.end(), rng);",
        "  std::string json = \"{\";",
        "  for (std::size_t i = 0; i < key_count; ++i) {",
        "    if (i) json += \",\";",
        "    std::size_t idx = order[i];",
        f"    json += \"\\\"\" + std::string(key_name(static_cast<int>(idx))) + \"\\\":\";",
        "    json += std::to_string(idx + 1000);",
        "  }",
        "  json += \"}\";",
        "  return json;",
        "}",
        "",
        "inline std::string build_json_partial(std::size_t present_count) {",
        "  std::string json = \"{\";",
        "  for (std::size_t i = 0; i < present_count && i < key_count; ++i) {",
        "    if (i) json += \",\";",
        f"    json += \"\\\"\" + std::string(key_name(static_cast<int>(i))) + \"\\\":\";",
        "    json += std::to_string(i);",
        "  }",
        "  json += \"}\";",
        "  return json;",
        "}",
        "",
        "inline std::string build_json_with_duplicates() {",
        "  std::string json = \"{\";",
        "  for (std::size_t i = 0; i < key_count; ++i) {",
        "    if (i) json += \",\";",
        f"    json += \"\\\"\" + std::string(key_name(static_cast<int>(i))) + \"\\\":\";",
        "    json += std::to_string(i);",
        "  }",
        "  // duplicate every key with a different value (must be ignored)",
        "  for (std::size_t i = 0; i < key_count; ++i) {",
        "    json += \",\";",
        f"    json += \"\\\"\" + std::string(key_name(static_cast<int>(i))) + \"\\\":\";",
        "    json += std::to_string(i + 99999);",
        "  }",
        "  json += \"}\";",
        "  return json;",
        "}",
        "",
        "}",
    ]
    (OUT / f"many_keys_{n}.hpp").write_text("\n".join(lines) + "\n")


def write_long_keys_header() -> None:
    keys = [
        ("a" * 63, "a"),
        ("b" + "y" * 62, "b"),
        ("c" + "z" * 30 + "w" * 31, "c"),
        ("prefix_" + "m" * 55, "p"),  # 63 chars total: prefix_ + 55 m's = 7+55=62... let me fix
    ]
    # Fix lengths
    keys = [
        ("a" * 63, "a63"),
        ("b" + "y" * 62, "b63"),
        (("c" * 16) + ("d" * 16) + ("e" * 16) + ("f" * 15), "blocks"),  # 63
        ("prefix_" + "m" * 56, "pref"),  # 7+56=63
        ("id", "short_id"),
        ("screen_name", "short_sn"),
        ("jo", "jo"),
        ("joe", "joe"),
    ]
    assert all(len(k[0]) <= 63 for k in keys[:4])
    key_args = ", ".join(f'"{k[0]}"' for k in keys)
    lines = [
        "// Auto-generated by scripts/generate_headers.py — do not edit.",
        "#pragma once",
        "#include <string>",
        "#include \"simdjson.h\"",
        "",
        "namespace generated_long_keys {",
        "",
        f"inline constexpr std::size_t key_count = {len(keys)};",
        "",
        f"using selector = simdjson::ondemand::key_selector<{key_args}>;",
        "",
        "struct expected_values {",
        "  std::string_view a63;",
        "  std::string_view b63;",
        "  std::string_view blocks;",
        "  std::string_view pref;",
        "  uint64_t short_id{};",
        "  std::string_view short_sn;",
        "  std::string_view jo;",
        "  std::string_view joe;",
        "};",
        "",
        "inline std::string build_json_reversed() {",
        "  return std::string(\"{\")",
        f'    + "\\"joe\\":\\"JOE\\""',
        f'    + ",\\"jo\\":\\"JO\\""',
        f'    + ",\\"screen_name\\":\\"lemire\\""',
        f'    + ",\\"id\\":42"',
        f'    + ",\\"{keys[3][0]}\\":\\"PREF\\""',
        f'    + ",\\"{keys[2][0]}\\":\\"BLOCKS\\""',
        f'    + ",\\"{keys[1][0]}\\":\\"BVAL\\""',
        f'    + ",\\"{keys[0][0]}\\":\\"AVAL\\""',
        "    + \"}\";",
        "}",
        "",
        "inline std::string build_json_joe_only() {",
        '  return R"({"joe":"only"})";',
        "}",
        "",
        "}",
    ]
    (OUT / "long_keys.hpp").write_text("\n".join(lines) + "\n")


def write_reflection_struct(n: int) -> None:
    members = []
    for i in range(n):
        k = f"k{i:03d}"
        members.append(f"  uint64_t {k}{{}};")
    struct_body = "\n".join(members)
    lines = [
        "// Auto-generated by scripts/generate_headers.py — do not edit.",
        "#pragma once",
        "#if SIMDJSON_STATIC_REFLECTION",
        "",
        f"struct reflected_record_{n} {{",
        struct_body,
        "};",
        "",
        f"inline std::string build_reflection_json_{n}(bool reverse = false) {{",
        "  std::string json = \"{\";",
        f"  for (std::size_t i = 0; i < {n}; ++i) {{",
        "    std::size_t idx = reverse ? (" + str(n) + " - 1 - i) : i;",
        f"    if (i) json += \",\";",
        f"    json += \"\\\"\" + std::string(\"k\") + (idx < 10 ? \"00\" : (idx < 100 ? \"0\" : \"\")) + std::to_string(idx) + \"\\\":\";",
        "    json += std::to_string(idx + 7);",
        "  }",
        "  json += \"}\";",
        "  return json;",
        "}",
        "",
        "#endif // SIMDJSON_STATIC_REFLECTION",
    ]
    # Fix the key formatting in reflection json builder
    lines_reflection = [
        "// Auto-generated by scripts/generate_headers.py — do not edit.",
        "#pragma once",
        "#if SIMDJSON_STATIC_REFLECTION",
        "#include <algorithm>",
        "#include <cstdint>",
        "#include <random>",
        "#include <string>",
        "#include <vector>",
        "",
        f"struct reflected_record_{n} {{",
        struct_body,
        "};",
        "",
        f"inline std::string build_reflection_json_{n}(bool reverse = false) {{",
        "  std::string json = \"{\";",
        f"  for (std::size_t i = 0; i < {n}; ++i) {{",
        f"    std::size_t idx = reverse ? ({n} - 1 - i) : i;",
        "    if (i) json += \",\";",
        "    char buf[8];",
        "    std::snprintf(buf, sizeof(buf), \"k%03zu\", idx);",
        "    json += \"\\\"\";",
        "    json += buf;",
        "    json += \"\\\":\";",
        "    json += std::to_string(idx + 7);",
        "  }",
        "  json += \"}\";",
        "  return json;",
        "}",
        "",

        "#endif // SIMDJSON_STATIC_REFLECTION",
    ]
    if n == 100:
        lines_reflection.extend([
            "",
            "inline std::string reflection_member_key(std::size_t idx) {",
            "  char buf[8];",
            "  std::snprintf(buf, sizeof(buf), \"k%03zu\", idx);",
            "  return buf;",
            "}",
            "",
            "inline std::string build_reflection_json_100_only(std::size_t idx, uint64_t value) {",
            "  return std::string(\"{\\\"\") + reflection_member_key(idx) + \"\\\":\" + std::to_string(value) + \"}\";",
            "}",
            "",
            "inline std::string build_reflection_json_100_except(std::size_t skip_idx) {",
            "  std::string json = \"{\";",
            "  bool first = true;",
            "  for (std::size_t i = 0; i < 100; ++i) {",
            "    if (i == skip_idx) continue;",
            "    if (!first) json += \",\";",
            "    first = false;",
            "    json += \"\\\"\" + reflection_member_key(i) + \"\\\":\" + std::to_string(i + 7);",
            "  }",
            "  json += \"}\";",
            "  return json;",
            "}",
            "",
            "inline std::string build_reflection_json_100_every_nth(std::size_t step) {",
            "  std::string json = \"{\";",
            "  bool first = true;",
            "  for (std::size_t i = 0; i < 100; i += step) {",
            "    if (!first) json += \",\";",
            "    first = false;",
            "    json += \"\\\"\" + reflection_member_key(i) + \"\\\":\" + std::to_string(i + 7);",
            "  }",
            "  json += \"}\";",
            "  return json;",
            "}",
            "",
            "inline std::string build_reflection_json_100_range(std::size_t begin, std::size_t end_exclusive) {",
            "  std::string json = \"{\";",
            "  bool first = true;",
            "  for (std::size_t i = begin; i < end_exclusive; ++i) {",
            "    if (!first) json += \",\";",
            "    first = false;",
            "    json += \"\\\"\" + reflection_member_key(i) + \"\\\":\" + std::to_string(i + 7);",
            "  }",
            "  json += \"}\";",
            "  return json;",
            "}",
            "",
            "inline std::string build_reflection_json_100_wrong_type(std::size_t idx, std::string_view literal) {",
            "  std::string json = \"{\";",
            "  for (std::size_t i = 0; i < 100; ++i) {",
            "    if (i) json += \",\";",
            "    json += \"\\\"\" + reflection_member_key(i) + \"\\\":\";",
            "    if (i == idx) json += std::string(literal);",
            "    else json += std::to_string(i + 7);",
            "  }",
            "  json += \"}\";",
            "  return json;",
            "}",
            "",
            "inline std::string build_reflection_json_100_with_noise(std::size_t noise_count) {",
            "  std::string json = \"{\";",
            "  bool first = true;",
            "  for (std::size_t n = 0; n < noise_count; ++n) {",
            "    if (!first) json += \",\";",
            "    first = false;",
            "    json += \"\\\"zz_noise_\" + std::to_string(n) + \"\\\":\" + std::to_string(n);",
            "  }",
            "  for (std::size_t i = 0; i < 100; ++i) {",
            "    if (!first) json += \",\";",
            "    first = false;",
            "    json += \"\\\"\" + reflection_member_key(i) + \"\\\":\" + std::to_string(i + 7);",
            "  }",
            "  json += \"}\";",
            "  return json;",
            "}",
            "",
            "inline std::string build_reflection_json_100_duplicates() {",
            "  std::string json = build_reflection_json_100(false);",
            "  json.pop_back();",
            "  for (std::size_t i = 0; i < 100; ++i) {",
            "    json += \",\\\"\" + reflection_member_key(i) + \"\\\":\" + std::to_string(i + 777);",
            "  }",
            "  json += \"}\";",
            "  return json;",
            "}",
            "",
            "inline std::string build_reflection_json_100_shuffled(uint64_t seed) {",
            "  std::vector<std::size_t> order(100);",
            "  for (std::size_t i = 0; i < 100; ++i) order[i] = i;",
            "  std::mt19937_64 rng(seed);",
            "  std::shuffle(order.begin(), order.end(), rng);",
            "  std::string json = \"{\";",
            "  for (std::size_t i = 0; i < 100; ++i) {",
            "    if (i) json += \",\";",
            "    std::size_t idx = order[i];",
            "    json += \"\\\"\" + reflection_member_key(idx) + \"\\\":\" + std::to_string(idx + 7);",
            "  }",
            "  json += \"}\";",
            "  return json;",
            "}",
        ])
    (OUT / f"reflection_struct_{n}.hpp").write_text("\n".join(lines_reflection) + "\n")


def main() -> None:
    OUT.mkdir(parents=True, exist_ok=True)
    for n in (10, 50, 100, 200, 250):
        write_many_keys_header(n)
    write_long_keys_header()
    for n in (10, 50, 100, 250):
        write_reflection_struct(n)
    print(f"Generated headers in {OUT}")


if __name__ == "__main__":
    main()